# This program displays basic math operators.


import math

# Addition
add_result = 2 + 3
print(f"Addition: 2 + 3 = {add_result}")

# Subtraction
subtract_result = 5 - 4
print(f"Subtraction: 5 - 4 = {subtract_result}")

# Multiplication
multiply_result = 7 * 6
print(f"Multiplication: 7 * 6 = {multiply_result}")

# Division (using numbers that give a decimal answer)
divide_result = 8 / 3
print(f"Division: 8 / 3 = {divide_result:.2f}")  # rounding to 2 decimal places

# Power (3^2)
power_result = 3**2
print(f"Power: 3^2 = {power_result}")

# Square root (√4)
square_root_result = math.sqrt(4)
print(f"Square Root: √4 = {square_root_result}")
